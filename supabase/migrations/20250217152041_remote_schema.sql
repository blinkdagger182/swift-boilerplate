CREATE POLICY "Enable users to view their own data only" ON "public"."transactions" FOR SELECT TO "authenticated" USING (("account_id" IN ( SELECT "transactions"."account_id"
   FROM "public"."accounts"
  WHERE ("accounts"."user_id" = ( SELECT "auth"."uid"() AS "uid")))));



